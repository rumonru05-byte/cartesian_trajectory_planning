from launch import LaunchDescription
from launch.substitutions import Command, FindExecutable, PathJoinSubstitution
from launch_ros.actions import Node
from launch_ros.substitutions import FindPackageShare


def generate_launch_description():
    # Get URDF via xacro
    robot_description_content = Command(
        [
            PathJoinSubstitution([FindExecutable(name="xacro")]),
            " ",
            PathJoinSubstitution(
                [
                    FindPackageShare("cartesian_trajectory_planning"),
                    "urdf",
                    "r6bot.urdf.xacro",
                ]
            ),
        ]
    )
    robot_description = {"robot_description": robot_description_content}

    send_circular_trajectory = Node(
        package="cartesian_trajectory_planning",
        executable="send_circular_trajectory",
        name="send_circular_trajectory",
        parameters=[robot_description],
    )

    nodes_to_start = [send_circular_trajectory]
    return LaunchDescription(nodes_to_start)
